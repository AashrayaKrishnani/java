public class Songs {
    private int id;
    private String title;
    private int track;
    private int album;

    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public String getTitle() {
        return title;
    }
    public void setTitle(String title) {
        this.title = title;
    }
    public int getTrack() {
        return track;
    }
    public void setTrack(int track) {
        this.track = track;
    }
    public int getAlbum() {
        return album;
    }
    public void setAlbum(int album) {
        this.album = album;
    }

    
}
